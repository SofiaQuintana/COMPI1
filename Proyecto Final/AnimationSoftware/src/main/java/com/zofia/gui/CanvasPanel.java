    private void imageBoxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_imageBoxItemStateChanged
        // TODO add your handling code here:
        if(imageBox.getSelectedIndex() >= 0) {
            if(change) {
                String value = (String) imageBox.getModel().getSelectedItem();
                setSelectedImage(value);
                setValues();
            }
        }
        
    }//GEN-LAST:event_imageBoxItemStateChanged

    private void tabbedPanelStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_tabbedPanelStateChanged
        // TODO add your handling code here:
        selected = canvases.get(tabbedPanel.getSelectedIndex());
        fillBoxes();
        addColorComboBox();
        setValues();
        selectedButton.setBackground(colors.getSelectedColor());
        eraser.setSelected(false);
        setCurrentImage(0, tabbedPanel.getSelectedIndex());
    }//GEN-LAST:event_tabbedPanelStateChanged


