    public CanvasPanel(List<Canvas> canvases) {
        initComponents();
        this.canvases = canvases;
        initValues();
        fillTabData();
        fillBoxes();
        setValues();
        setCurrentImage(0, 0);
        change = true;
    }
    
    private void initValues() {
        this.selected = canvases.get(0);
        this.canvasImages = selected.getTime().getImages();
        this.images = new ArrayList<>();
        this.imageTitle = new String[canvases.size()];
        this.panel = new ArrayList<>();
        this.selectedImage = canvasImages.get(0);
        addPanels();
    }
    
    private void fillTabData() {
        for (int i = 0; i < imageTitle.length; i++) {
            imageTitle[i] = canvases.get(i).getId();
            panel.add(new JPanel());
            tabbedPanel.addTab(imageTitle[i], panel.get(i));
        }
    }
    
    private void addPanels() {
        for(Canvas canvas : canvases) {
            for(Image image : canvas.getTime().getImages()) {
                image.setImage(new ImagePanel(this, canvas.getColor(),canvas.getSquare()));
                images.add(image.getImage());
            }
        }
    }
    
    private void setCurrentImage(int index, int panel) {
        ImagePanel image = canvasImages.get(index).getImage();
        image.setBorder(false);
        this.panel.get(panel).removeAll();
        JScrollPane scroll = new JScrollPane();
        scroll.setViewportView(image);
        this.panel.get(panel).setLayout(new BorderLayout());
        this.panel.get(panel).add(scroll, BorderLayout.CENTER);
        this.panel.get(panel).repaint();
        this.panel.get(panel).revalidate();
    }
    
    private void fillBoxes() {
        change = false;
        canvasImages = selected.getTime().getImages();
        this.imageBox.removeAllItems();
        this.start.removeAllItems();
        for(Image image : canvasImages) {
            imageBox.addItem(image.getId());
            start.addItem(image.getId());
            end.addItem(image.getId());
        }
        setInitialBoxesValues();
        addColorComboBox();
        change = true;
    }
    
    private void setValues() {
        amountField.setText(String.valueOf(canvasImages.size()));
        durationField.setText(String.valueOf(selectedImage.getDuration()));
    }
    
    public void addColorComboBox(){
        colors = new Colors(selected.getColorsTable());
        colorPanel.removeAll();
        colorPanel.add(colors);
        colorPanel.repaint();
        colorPanel.revalidate();
        colors.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                    selectedButton.setBackground(colors.getSelectedColor());
           }
        });     
    }
    
    private void setInitialBoxesValues() {
        imageBox.setSelectedIndex(0);
        start.setSelectedItem(selected.getTime().getStart());
        end.setSelectedItem(selected.getTime().getEnd());
    }

    public JCheckBox getEraser() {
        return eraser;
    }

    public JButton getSelectedButton() {
        return selectedButton;
    }
    
    private void setSelectedImage(String id) {
        int index = 0;
        for(Image image : selected.getTime().getImages()) {
            if(image.getId().equals(id)) {
                selectedImage = image;
            }
            index++;
        }
        setCurrentImage(index-1, tabbedPanel.getSelectedIndex());
    }
    
